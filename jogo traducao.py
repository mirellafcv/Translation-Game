import sounddevice as sd
import numpy as np
import scipy.io.wavfile as wav
import speech_recognition as sr
from googletrans import Translator
import random


vidas = 3  # número de vidas do jogador

while True:

    duration = 5  # segundos de gravação
    sample_rate = 44100
    words_by_level = {
        "fácil": ["gato", "pássaro", "pão", "leite", "sol"],
        "médio": ["casa", "escola", "amigo", "janela", "amarelo"],
        "difícil": ["tecnologia", "universidade", "informação", "pronúncia", "passaporte"]
    }

    level = input("Escolha o nível de dificuldade (fácil, médio, difícil) ou 'sair' para encerrar: ").lower()
    if level == "sair":
        break
    lang = input("Digite o código do idioma para traduzir ('ro' para romeno ou 'en' para inglês): ").lower()
    palavra = random.choice(words_by_level[level])

